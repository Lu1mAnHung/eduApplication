package com.edu.model;

public class StudentGradeInfo {
 private String c_name;

 private Integer g_grade;

 private String s_cid;

 private Integer g_staus;


public Integer getG_grade() {
	return g_grade;
}

public void setG_grade(Integer g_grade) {
	this.g_grade = g_grade;
}

public String getS_cid() {
	return s_cid;
}

public void setS_cid(String s_cid) {
	this.s_cid = s_cid;
}

public Integer getG_staus() {
	return g_staus;
}

public void setG_staus(Integer g_staus) {
	this.g_staus = g_staus;
}

public String getC_name() {
	return c_name;
}

public void setC_name(String c_name) {
	this.c_name = c_name;
}
}
